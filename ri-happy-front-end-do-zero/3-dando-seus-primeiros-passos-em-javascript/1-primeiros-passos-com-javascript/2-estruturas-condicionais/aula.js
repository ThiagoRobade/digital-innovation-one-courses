// Boolean

const numero = 25;
const numeroDivisivelPor5 = (numero % 5) === 0;

if  (numeroDivisivelPor5) {
    console.log("Sim");
} else {
    console.log("Não");
}

// Else If

if (numero === 0) {
    console.log("O número é invalido");
} else if (numeroDivisivelPor5) {
    console.log("Sim");
} else {
    console.log("Não");
}



